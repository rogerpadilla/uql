import { describe, expect, it } from 'vitest';
import { Entity, Field, Id, ManyToOne } from '../entity/index.js';
import type { ColumnNode, IndexNode, TableNode } from '../schema/types.js';
import { SqlSchemaGenerator } from './schemaGenerator.js';

// Test entities
@Entity()
class TestUser {
  @Id()
  id?: number;

  @Field({ length: 100 })
  name?: string;

  @Field({ unique: true })
  email?: string;

  @Field({ nullable: false })
  password?: string;

  @Field({ onInsert: Date.now })
  createdAt?: number;
}

@Entity({ name: 'blog_posts' })
class TestPost {
  @Id({ onInsert: () => crypto.randomUUID(), length: 36 })
  id?: string;

  @Field()
  title?: string;

  @Field({ columnType: 'text' })
  content?: string;

  @Field({ type: 'jsonb' })
  metadata?: Record<string, unknown>;

  @Field({ references: () => TestUser })
  authorId?: number;

  @ManyToOne()
  author?: TestUser;
}

describe('SqlSchemaGenerator (Postgres)', () => {
  const generator = new SqlSchemaGenerator('postgres');

  it('should generate CREATE TABLE for simple entity', () => {
    const sql = generator.generateCreateTable(TestUser);

    expect(sql).toContain('CREATE TABLE "TestUser"');
    expect(sql).toContain('"id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY');
    expect(sql).toContain('"name" VARCHAR(100)');
    expect(sql).toContain('"email" TEXT UNIQUE');
    expect(sql).toContain('"password" TEXT NOT NULL');
    expect(sql).toContain('"createdAt"');
  });

  it('should generate CREATE TABLE with UUID primary key', () => {
    const sql = generator.generateCreateTable(TestPost);

    expect(sql).toContain('CREATE TABLE "blog_posts"');
    expect(sql).toContain('"id" VARCHAR(36) PRIMARY KEY');
    expect(sql).toContain('"title" TEXT');
    expect(sql).toContain('"content" TEXT');
    expect(sql).toContain('"metadata" JSONB');
    expect(sql).toContain('"authorId" BIGINT');
  });

  it('should generate DROP TABLE statement', () => {
    const sql = generator.generateDropTable(TestUser);

    expect(sql).toBe('DROP TABLE IF EXISTS "TestUser";');
  });

  it('should generate CREATE INDEX statement', () => {
    const sql = generator.generateCreateIndex('users', {
      name: 'idx_users_email',
      columns: ['email'],
      unique: true,
    });

    expect(sql).toBe('CREATE UNIQUE INDEX IF NOT EXISTS "idx_users_email" ON "users" ("email");');
  });

  it('should generate CREATE INDEX for HNSW vector index', () => {
    const sql = generator.generateCreateIndex('articles', {
      name: 'idx_articles_embedding_hnsw',
      columns: ['embedding'],
      unique: false,
      type: 'hnsw',
      distance: 'cosine',
    });
    expect(sql).toBe(
      'CREATE INDEX IF NOT EXISTS "idx_articles_embedding_hnsw" ON "articles" USING hnsw ("embedding" vector_cosine_ops);',
    );
  });

  it('should generate CREATE INDEX for HNSW with tuning params', () => {
    const sql = generator.generateCreateIndex('articles', {
      name: 'idx_embedding',
      columns: ['embedding'],
      unique: false,
      type: 'hnsw',
      distance: 'l2',
      m: 16,
      efConstruction: 64,
    });
    expect(sql).toBe(
      'CREATE INDEX IF NOT EXISTS "idx_embedding" ON "articles" USING hnsw ("embedding" vector_l2_ops) WITH (m = 16, ef_construction = 64);',
    );
  });

  it('should generate CREATE INDEX for IVFFlat', () => {
    const sql = generator.generateCreateIndex('articles', {
      name: 'idx_embedding_ivf',
      columns: ['embedding'],
      unique: false,
      type: 'ivfflat',
      distance: 'inner',
      lists: 100,
    });
    expect(sql).toBe(
      'CREATE INDEX IF NOT EXISTS "idx_embedding_ivf" ON "articles" USING ivfflat ("embedding" vector_ip_ops) WITH (lists = 100);',
    );
  });

  it('should not emit operator classes or WITH params for non-Postgres dialects', () => {
    const mysqlGenerator = new SqlSchemaGenerator('mysql');
    const sql = mysqlGenerator.generateCreateIndex('articles', {
      name: 'idx_embedding',
      columns: ['embedding'],
      unique: false,
      type: 'hnsw',
      distance: 'cosine',
      m: 16,
      efConstruction: 64,
    });
    // MySQL: no operator class, no WITH params — just USING
    expect(sql).toBe('CREATE INDEX `idx_embedding` ON `articles` USING hnsw (`embedding`);');
  });
  /** Build a minimal TableNode mock with an id + embedding column and the given vector indexes. */
  function buildVectorTableNode(indexes: Partial<IndexNode>[]): TableNode {
    const table = {
      name: 'embeddings',
      primaryKey: [],
      indexes: [],
      outgoingRelations: [],
      incomingRelations: [],
    } as unknown as TableNode;
    (table as { columns: Map<string, ColumnNode> }).columns = new Map<string, ColumnNode>([
      [
        'id',
        {
          name: 'id',
          type: { category: 'integer' },
          nullable: false,
          isPrimaryKey: true,
          isAutoIncrement: true,
          isUnique: false,
          table,
        } as ColumnNode,
      ],
      [
        'embedding',
        {
          name: 'embedding',
          type: { category: 'vector', length: 1536 },
          nullable: false,
          isPrimaryKey: false,
          isAutoIncrement: false,
          isUnique: false,
          table,
        } as ColumnNode,
      ],
    ]);
    (table as { indexes: IndexNode[] }).indexes = indexes.map(
      (idx) =>
        ({
          name: 'idx_vec',
          table,
          columns: [{ name: 'embedding' }],
          unique: false,
          type: 'vector',
          ...idx,
        }) as IndexNode,
    );
    return table;
  }

  it('should emit inline VECTOR INDEX for MariaDB', () => {
    const mariaGenerator = new SqlSchemaGenerator('mariadb');
    const sql = mariaGenerator.generateCreateTableFromNode(buildVectorTableNode([{}]));
    expect(sql).toContain('VECTOR INDEX (`embedding`)');
    expect(sql).not.toContain('CREATE INDEX');
    expect(sql).not.toContain('CREATE EXTENSION');
  });

  it('should emit inline VECTOR INDEX with M and DISTANCE for MariaDB', () => {
    const mariaGenerator = new SqlSchemaGenerator('mariadb');
    const sql = mariaGenerator.generateCreateTableFromNode(buildVectorTableNode([{ distance: 'cosine', m: 8 }]));
    expect(sql).toContain('VECTOR INDEX (`embedding`) M=8 DISTANCE=cosine');
  });

  it('should emit VECTOR INDEX with l2 as euclidean for MariaDB', () => {
    const mariaGenerator = new SqlSchemaGenerator('mariadb');
    const sql = mariaGenerator.generateCreateTableFromNode(buildVectorTableNode([{ distance: 'l2' }]));
    expect(sql).toContain('VECTOR INDEX (`embedding`) DISTANCE=euclidean');
  });

  it('should prepend CREATE EXTENSION for Postgres tables with vector columns', () => {
    const sql = generator.generateCreateTableFromNode(buildVectorTableNode([]));
    expect(sql).toMatch(/^CREATE EXTENSION IF NOT EXISTS vector;/);
    expect(sql).toContain('CREATE TABLE');
  });

  it('should generate CREATE TABLE with IF NOT EXISTS', () => {
    const sql = generator.generateCreateTable(TestUser, { ifNotExists: true });
    expect(sql).toContain('CREATE TABLE IF NOT EXISTS "TestUser"');
  });

  it('should handle already-typed column schemas', () => {
    const sql = generator.generateAlterTable({
      tableName: 'users',
      type: 'alter',
      columnsToAdd: [
        {
          name: 'profile_data',
          type: 'JSONB',
          nullable: true,
          isPrimaryKey: false,
          isAutoIncrement: false,
          isUnique: false,
        },
      ],
    });
    // Should NOT be JSONB(JSONB)
    expect(sql[0]).toContain('ADD COLUMN "profile_data" JSONB');
    expect(sql[0]).not.toContain('JSONB(');
  });

  it('should generate ALTER TABLE DOWN statements for column additions', () => {
    const sql = generator.generateAlterTableDown({
      tableName: 'users',
      type: 'alter',
      columnsToAdd: [
        {
          name: 'profile_data',
          type: 'JSONB',
          nullable: true,
          isPrimaryKey: false,
          isAutoIncrement: false,
          isUnique: false,
        },
      ],
    });
    expect(sql[0]).toBe('ALTER TABLE "users" DROP COLUMN "profile_data";');
  });

  it('should generate ALTER TABLE DOWN statements for column alterations', () => {
    const sql = generator.generateAlterTableDown({
      tableName: 'users',
      type: 'alter',
      columnsToAlter: [
        {
          from: {
            name: 'age',
            type: 'INTEGER',
            nullable: true,
            isPrimaryKey: false,
            isAutoIncrement: false,
            isUnique: false,
          },
          to: {
            name: 'age',
            type: 'BIGINT',
            nullable: false,
            isPrimaryKey: false,
            isAutoIncrement: false,
            isUnique: false,
          },
        },
      ],
    });
    expect(sql.length).toBeGreaterThan(0);
    expect(sql[0]).toContain('ALTER TABLE');
    expect(sql[0]).toContain('"age"');
    expect(sql[0]).toContain('INTEGER');
  });

  it('should generate ALTER TABLE DOWN statements for index additions', () => {
    const sql = generator.generateAlterTableDown({
      tableName: 'users',
      type: 'alter',
      indexesToAdd: [{ name: 'idx_users_email', columns: ['email'], unique: true }],
    });
    expect(sql[0]).toBe('DROP INDEX IF EXISTS "idx_users_email";');
  });

  it('should add TODO comment for dropped columns/indexes', () => {
    const sql1 = generator.generateAlterTableDown({
      tableName: 'users',
      type: 'alter',
      columnsToDrop: ['old_column'],
    });
    expect(sql1[0]).toContain('TODO');

    const sql2 = generator.generateAlterTableDown({
      tableName: 'users',
      type: 'alter',
      indexesToDrop: ['idx_old'],
    });
    expect(sql2[0]).toContain('TODO');
  });

  it('should generate ALTER TABLE statements for dropping columns and indexes', () => {
    const sql = generator.generateAlterTable({
      tableName: 'users',
      type: 'alter',
      columnsToDrop: ['old_col'],
      indexesToDrop: ['idx_old'],
      indexesToAdd: [{ name: 'idx_new', columns: ['name'], unique: false }],
    });
    expect(sql).toContain('ALTER TABLE "users" DROP COLUMN "old_col";');
    expect(sql).toContain('DROP INDEX IF EXISTS "idx_old";');
    expect(sql).toContain('CREATE INDEX IF NOT EXISTS "idx_new" ON "users" ("name");');
  });
});

describe('SqlSchemaGenerator (MySQL)', () => {
  const generator = new SqlSchemaGenerator('mysql');

  it('should generate CREATE TABLE for simple entity', () => {
    const sql = generator.generateCreateTable(TestUser);

    expect(sql).toContain('CREATE TABLE `TestUser`');
    expect(sql).toContain('`id` BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY');
    expect(sql).toContain('`name` VARCHAR(100)');
    expect(sql).toContain('`email` VARCHAR(255) UNIQUE');
    expect(sql).toContain('ENGINE=InnoDB');
  });

  it('should generate boolean as TINYINT(1)', () => {
    const boolType = generator.getSqlType({ type: Boolean }, Boolean);
    expect(boolType).toBe('TINYINT(1)');
  });

  it('should generate DROP INDEX with ON table', () => {
    const sql = generator.generateDropIndex('users', 'idx_email');
    expect(sql).toBe('DROP INDEX `idx_email` ON `users`;');
  });
});

describe('SqlSchemaGenerator (SQLite)', () => {
  const generator = new SqlSchemaGenerator('sqlite');

  it('should generate CREATE TABLE for simple entity', () => {
    const sql = generator.generateCreateTable(TestUser);

    expect(sql).toContain('CREATE TABLE `TestUser`');
    expect(sql).toContain('`id` INTEGER PRIMARY KEY AUTOINCREMENT');
    expect(sql).toContain('`name` TEXT');
    expect(sql).toContain('`email` TEXT UNIQUE');
  });

  it('should use TEXT for most types (SQLite dynamic typing)', () => {
    expect(generator.getSqlType({ columnType: 'varchar' }, String)).toBe('TEXT');
    expect(generator.getSqlType({ columnType: 'json' }, undefined)).toBe('TEXT');
    expect(generator.getSqlType({ columnType: 'uuid' }, undefined)).toBe('TEXT');
  });

  it('should use INTEGER for numeric types', () => {
    expect(generator.getSqlType({ columnType: 'int' }, undefined)).toBe('INTEGER');
    expect(generator.getSqlType({ columnType: 'bigint' }, undefined)).toBe('INTEGER');
    expect(generator.getSqlType({ type: Boolean }, Boolean)).toBe('INTEGER');
  });
});

describe('SqlSchemaGenerator Integration', () => {
  const generator = new SqlSchemaGenerator('postgres');

  it('should generate CREATE TABLE from TableNode', () => {
    const table = {
      name: 'users',
      columns: new Map<string, ColumnNode>(),
      primaryKey: [],
      indexes: [],
      incomingRelations: [],
      outgoingRelations: [],
    } as unknown as TableNode;
    (table as { columns: Map<string, ColumnNode> }).columns = new Map<string, ColumnNode>([
      [
        'id',
        {
          name: 'id',
          type: { category: 'integer', size: 'big' },
          isPrimaryKey: true,
          isAutoIncrement: true,
          nullable: false,
          table,
        } as ColumnNode,
      ],
      ['name', { name: 'name', type: { category: 'string', length: 100 }, nullable: true, table } as ColumnNode],
    ]);
    (table as { primaryKey: ColumnNode[] }).primaryKey = [table.columns.get('id')!];

    const sql = generator.generateCreateTableFromNode(table);
    expect(sql).toContain('CREATE TABLE "users"');
    expect(sql).toContain('"id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY');
    expect(sql).toContain('"name" VARCHAR(100)');
  });

  it('should generate CREATE INDEX from IndexNode', () => {
    const table = { name: 'users' } as TableNode;
    const index: IndexNode = {
      name: 'idx_name',
      table,
      columns: [{ name: 'name' } as ColumnNode],
      unique: true,
    };
    const sql = generator.generateCreateIndexFromNode(index);
    expect(sql).toBe('CREATE UNIQUE INDEX "idx_name" ON "users" ("name");');
  });

  it('should generate DROP TABLE from TableNode', () => {
    const table = { name: 'users' } as TableNode;
    const sql = generator.generateDropTableFromNode(table, { ifExists: true });
    expect(sql).toBe('DROP TABLE IF EXISTS "users";');
  });
});
